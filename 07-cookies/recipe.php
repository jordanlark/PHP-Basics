      <h1>Secret Recipe!</h1>
        <h2>Chocolate chip</h2>

        <p>Ingredients:</p>
        <ul>
          <li>3/4 c. unsalted butter, softened</li>
          <li>3/4 c. brown sugar</li>
          <li>1/4 c. granulated sugar</li>
          <li>1 egg</li>
          <li>2 tsp. vanilla extract</li>
          <li>2 c. all purpose flour</li>
          <li>1 tsp. baking soda</li>
          <li>2 tsp. cornstarch</li>
          <li>1/2 tsp. salt</li>
          <li>1 c. bittersweet chocolate chips
</li>
        </ul>

<p>Directions:</p>
<ol>
  <li>  Preheat oven to 350 degrees F.
</li>
  <li>  In the bowl of a stand mixer fitted with a paddle attachment, cream together butter and sugars until fluffy and light in color. Add egg and vanilla and blend in.
</li>
  <li>  Mix in flour, cornstarch, baking soda and salt. Stir in chocolate chunks.
</li>
  <li>  Using a standard-sized cookie scoop or tablespoon, drop dough onto a prepared baking sheet.  Bake for 8-10 minutes, until barely golden brown around the edges.  (The tops will not brown, but do NOT cook longer than ten minutes.)
</li>
  <li>  Let cool, on the sheet, on a wire rack for five minutes.  Remove from baking sheet and let cool completely.  Makes approximately 3 dozen.  Try not to eat them all.
</li>
</ol>





